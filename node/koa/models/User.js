const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// 实例化
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = users = mongoose.model('user', UserSchema);
